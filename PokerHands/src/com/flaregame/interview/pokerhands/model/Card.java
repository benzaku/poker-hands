package com.flaregame.interview.pokerhands.model;

public class Card {
	private Integer value;
	private Suit suit;

	public Integer getValue() {
		return value;
	}

	public void setValue(Integer value) {
		this.value = value;
	}

	public Suit getSuit() {
		return suit;
	}

	public void setSuit(Suit suit) {
		this.suit = suit;
	}
	
}
